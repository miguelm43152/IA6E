# podemos usar la función len() para contar el numero de elementos que tiene una lista. Por ejemplo:

colores = ['rojo', 'azul', 'verde', 'amarillo', 'marrón',
'lila', 'negro', 'rosa', 'blanco', 'naranja']

n = len(colores)
